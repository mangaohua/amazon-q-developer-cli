export const getKeySymbol = (key: string) => {
  switch (key) {
    case "control":
      return "⌃";
    case "shift":
      return "⇧";
    case "command":
      return "⌘";
    case "option":
      return "⌥";
    default:
      return key;
  }
};

export const getKeyName = (keyCode: number): string => keyCodeToName[keyCode];

